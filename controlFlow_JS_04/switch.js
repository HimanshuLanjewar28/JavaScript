// switch case

const month = "Apr"
switch (month) {
    case "Jan": 
    console.log("January");
        break;
    case "Feb": 
    console.log("February");
        break;
    case "Mar": 
    console.log("March");
        break;
    case "Apr": 
    console.log("April");
        break;
    case "May": 
    console.log("May");
        break;

    default:
        console.log("Default case match");
        break;
}

